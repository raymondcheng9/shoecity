				<td valign="top" colspan="3" style="padding-left: 5px"><h2>Welcome to AbbySunderland.com</h2>
				
				<p>My name is Abigail "Abby" Sunderland and it is my dream to sail around the world. I’ve been around the water and on boats since I was 6 months old. I began single-handing when I was 13. I had this idea several years ago, even before my brother, <a href="http://www.zacsunderland.com/" target="_blank">Zac Sunderland</a>. But watching him do it in 2009 made me realize my dream could come true with lots of hard work, support and perseverance. 
In January 2010, I set sail around the world on <a href="/abbys-boat.php">"Wild Eyes"</a>, an Open 40 racing sailboat. I am attempting to become the world's youngest solo circumnavigator! This will be quite the adventure! I invite you to follow my journey as I will be posting pictures and videos, <a href="http://soloround.blogspot.com/" target="_blank">blogging</a> as much as I can and keeping you updated on my location.  
I am amazed at all of the support I am getting from all over the world. Thank you to my family, friends, supporters, <a href="/sponsors.php">sponsors</a> and Team Abby. Without all of you, this would all be just a dream.<span style="padding-left: 330px"><span><b>- Love, Abby</b>
